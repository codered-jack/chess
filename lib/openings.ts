export interface OpeningInfo {
  name: string
  eco: string
}

/** Returns the best (longest-match) opening for the given SAN move list. */
export function getOpeningName(sanMoves: string[]): OpeningInfo | null {
  if (sanMoves.length === 0) return null

  let best: OpeningInfo | null = null
  let bestLen = 0

  for (const [movesStr, name, eco] of OPENINGS) {
    const opMoves = movesStr.split(' ')
    if (opMoves.length > sanMoves.length) continue
    const matches = opMoves.every((m, i) => m === sanMoves[i])
    if (matches && opMoves.length > bestLen) {
      bestLen = opMoves.length
      best = { name, eco }
    }
  }

  return best
}
